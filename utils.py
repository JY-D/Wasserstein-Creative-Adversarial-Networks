def get_max_end(path_dir, num_len=3, fname_pattern='*.jpg'):
  max_ = 0
  for f in glob(path_dir + fname_pattern):
    curr = int(f[-num_len-4:-4])
    if curr > max_:
      max_ = curr
  return max_

